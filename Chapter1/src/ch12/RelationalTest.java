package ch12;

public class RelationalTest {

	public static void main(String[] args) {
		
		int num1 = 5;
		int num2 = 3;
		
		boolean value = (num1 > num2);
		System.out.println(value);
		
		System.out.println(num1 < num2);
		System.out.println(num1 >= num2);
		System.out.println(num1 <= num2);
		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
		
		// boolean 타입으로 변수를 선언해서 출력 결과가 true나 false로 나타나게 된다.
		

	}

}
